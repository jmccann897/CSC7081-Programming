package lecture6_06Oct2022;

public class Lecture6_06Oct2022 {

	public static void main(String[] args) {
		// Do While demo
		//When its false, do while will complete the code block once wheras a normal while loop exits
		
		int num = 1;
		do {
			System.out.println(num);
			num++;
		} while (num < 11);

		System.out.println("End of Program");
	}

}
